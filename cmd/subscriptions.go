func runSubscriptionsGet(_ *cobra.Command, args []string) error {
	client, err := mollieclient.New(cfg, flagAPIKey, flagLive, flagProfile)
	if err != nil {
		return err
	}

	resp, err := client.Subscriptions.Get(context.Background(), args[0], args[1], nil, nil)
	if err != nil {
		return fmt.Errorf("getting subscription: %w", err)
	}
	s := resp.GetSubscriptionResponse()
	if s == nil {
		return fmt.Errorf("subscription not found")
	}

	switch resolvedOutput() {
	case output.FormatJSON:
		return output.PrintJSON(s)
	default:
		timesStr := "—"
		if t := s.GetTimes(); t != nil {
			timesStr = strconv.FormatInt(*t, 10)
		}
		timesRemainingStr := "—"
		if t := s.GetTimesRemaining(); t != nil {
			timesRemainingStr = strconv.FormatInt(*t, 10)
		}
		output.PrintTable(
			[]string{"FIELD", "VALUE"},
			[][]string{
				{"ID", s.GetID()},
				{"Mode", string(s.GetMode())},
				{"Status", string(s.GetStatus())},
				{"Amount", formatAmount(s.GetAmount())},
				{"Interval", s.GetInterval()},
				{"Description", s.GetDescription()},
				{"Start Date", s.GetStartDate()},
				{"Next Payment Date", derefOpt(s.GetNextPaymentDate())},
				{"Times", timesStr},
				{"Times Remaining", timesRemainingStr},
				{"Customer ID", s.GetCustomerID()},
				{"Mandate ID", derefOpt(s.GetMandateID())},
				{"Webhook URL", derefOpt(s.GetWebhookURL())},
				{"Created At", s.GetCreatedAt()},
				{"Canceled At", derefOpt(s.GetCanceledAt())},
			},
			!flagLive,
		)
	}
	return nil
}

func runSubscriptionsUpdate(_ *cobra.Command, args []string) error {
	customerID, subscriptionID := args[0], args[1]

	if (subUpdateAmount == "") != (subUpdateCurrency == "") {
		return fmt.Errorf("--amount and --currency must both be set")
	}

	client, err := mollieclient.New(cfg, flagAPIKey, flagLive, flagProfile)
	if err != nil {
		return err
	}

	body := &operations.UpdateSubscriptionRequestBody{}
	if subUpdateAmount != "" {
		body.Amount = &components.Amount{
			Currency: subUpdateCurrency,
			Value:    subUpdateAmount,
		}
	}
	if subUpdateInterval != "" {
		body.Interval = &subUpdateInterval
	}
	if subUpdateDescription != "" {
		body.Description = &subUpdateDescription
	}
	if subUpdateStartDate != "" {
		body.StartDate = &subUpdateStartDate
	}
	if subUpdateTimes > 0 {
		body.Times = &subUpdateTimes
	}
	if subUpdateWebhookURL != "" {
		body.WebhookURL = &subUpdateWebhookURL
	}
	if subUpdateMandateID != "" {
		body.MandateID = &subUpdateMandateID
	}
	if subUpdateMetadata != "" {
		meta, err := parseMetadata(subUpdateMetadata)
		if err != nil {
			return fmt.Errorf("invalid --metadata: %w", err)
		}
		body.Metadata = &meta
	}

	resp, err := client.Subscriptions.Update(context.Background(), customerID, subscriptionID, nil, body)
	if err != nil {
		return fmt.Errorf("updating subscription: %w", err)
	}

	switch resolvedOutput() {
	case output.FormatJSON:
		return output.PrintJSON(resp.GetSubscriptionResponse())
	default:
		fmt.Printf("✓ Subscription %s updated\n", subscriptionID)
	}
	return nil
}

func runSubscriptionsCancel(_ *cobra.Command, args []string) error {
	customerID, subscriptionID := args[0], args[1]

	if !subCancelConfirm {
		confirmed, err := prompt.Confirm(fmt.Sprintf("Cancel subscription %s for customer %s?", subscriptionID, customerID))
		if err != nil {
			if errors.Is(err, huh.ErrUserAborted) {
				fmt.Println("Cancelled.")
				return nil
			}
			return err
		}
		if !confirmed {
			fmt.Println("Cancelled.")
			return nil
		}
	}

	client, err := mollieclient.New(cfg, flagAPIKey, flagLive, flagProfile)
	if err != nil {
		return err
	}

	if _, err := client.Subscriptions.Cancel(context.Background(), customerID, subscriptionID, nil, nil); err != nil {
		return fmt.Errorf("canceling subscription: %w", err)
	}

	fmt.Printf("✓ Subscription %s canceled\n", subscriptionID)
	return nil
}

