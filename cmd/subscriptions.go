func runSubscriptionsCreate(cmd *cobra.Command, args []string) error {
	customerID := args[0]

	applyCreateDefaults(cmd,
		&subCreateDescription, &subCreateAmount, &subCreateCurrency,
		nil, &subCreateWebhookURL, // subscriptions have no redirect-url
	)

	// Validate fields that are required but may come from defaults.
	switch {
	case subCreateAmount == "":
		return fmt.Errorf("required flag \"amount\" not set and no default configured (run `mollie defaults set`)")
	case subCreateCurrency == "":
		return fmt.Errorf("required flag \"currency\" not set and no default configured (run `mollie defaults set`)")
	case subCreateDescription == "":
		return fmt.Errorf("required flag \"description\" not set and no default configured (run `mollie defaults set`)")
	}

	client, err := mollieclient.New(cfg, flagAPIKey, flagLive, flagProfile)
	if err != nil {
		return err
	}

	req := &components.SubscriptionRequest{
		Amount: &components.Amount{
			Currency: subCreateCurrency,
			Value:    subCreateAmount,
		},
		Interval:    &subCreateInterval,
		Description: &subCreateDescription,
	}
	if subCreateStartDate != "" {
		req.StartDate = &subCreateStartDate
	}
	if subCreateTimes > 0 {
		req.Times = &subCreateTimes
	}
	if subCreateMethod != "" {
		m := components.SubscriptionMethod(subCreateMethod)
		req.Method = &m
	}
	if subCreateWebhookURL != "" {
		req.WebhookURL = &subCreateWebhookURL
	}
	if subCreateMandateID != "" {
		req.MandateID = &subCreateMandateID
	}
	if subCreateMetadata != "" {
		meta, err := parseMetadata(subCreateMetadata)
		if err != nil {
			return fmt.Errorf("invalid --metadata: %w", err)
		}
		req.Metadata = &meta
	}

	resp, err := client.Subscriptions.Create(context.Background(), customerID, nil, req)
	if err != nil {
		return fmt.Errorf("creating subscription: %w", err)
	}
	s := resp.GetSubscriptionResponse()
	if s == nil {
		return fmt.Errorf("unexpected empty response from API")
	}

	switch resolvedOutput() {
	case output.FormatJSON:
		return output.PrintJSON(s)
	default:
		output.PrintTable(
			[]string{"ID", "STATUS", "AMOUNT", "INTERVAL", "DESCRIPTION"},
			[][]string{{
				s.GetID(),
				string(s.GetStatus()),
				formatAmount(s.GetAmount()),
				s.GetInterval(),
				s.GetDescription(),
			}},
			!flagLive,
		)
	}
	return nil
}

func runSubscriptionsList(_ *cobra.Command, args []string) error {
	client, err := mollieclient.New(cfg, flagAPIKey, flagLive, flagProfile)
	if err != nil {
		return err
	}

	req := operations.ListSubscriptionsRequest{
		CustomerID: args[0],
		Limit:      &subListLimit,
	}
	if subListFrom != "" {
		req.From = &subListFrom
	}

	resp, err := client.Subscriptions.List(context.Background(), req)
	if err != nil {
		return fmt.Errorf("listing subscriptions: %w", err)
	}
	if resp.Object == nil {
		return nil
	}

	subListEmbedded := resp.Object.GetEmbedded()
	subs := subListEmbedded.GetSubscriptions()

	switch resolvedOutput() {
	case output.FormatJSON:
		return output.PrintJSON(resp.Object)
	default:
		rows := make([][]string, 0, len(subs))
		for _, s := range subs {
			rows = append(rows, []string{
				s.GetID(),
				string(s.GetStatus()),
				formatAmount(s.GetAmount()),
				s.GetInterval(),
				s.GetDescription(),
			})
		}
		output.PrintTable(
			[]string{"ID", "STATUS", "AMOUNT", "INTERVAL", "DESCRIPTION"},
			rows,
			!flagLive,
		)
	}
	return nil
}

